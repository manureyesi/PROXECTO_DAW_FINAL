    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        
        try{
            /*Limpar Lista*/
            int a=modelo.getRowCount();
            
            for (int i = 0; i < a; i++) {
                modelo.removeRow(0);
            }
            
        }catch(Exception ex){
            System.err.println(ex.getMessage());
        }
        
        try{
            
            String cod, nome, Categoria, Stock, SinIVA, iva, ConIVA;
            
            /*Conexion contra DB*/
            db.consultas con = new db.consultas();
            
            System.out.println("Introduciendo productos en lista");
             
            /* contador productos */
            int cont = 0;
            
            /* Consulta Productos */
            ResultSet rs = con.select("productos", "1");
            
            while(rs.next()){
                
                cont++;
                
                ResultSet rsaux = con.select("categorias", "1 ORDER BY 'cod' ASC");
                
                String NombreCategoria = "";
                
                while(rsaux.next()){
                    
                    if(rs.getInt("codCategoria") == rsaux.getInt("cod")){
                        NombreCategoria = rsaux.getString("nombre");
                    }
                    
                }
                
                Productos p1 = new Productos(rs.getString("cod"), rs.getString("nombre"), rs.getDouble("precioSin"), rs.getInt("stock"), NombreCategoria);
                
                /* Redondear */
                Redondear redondear = new Redondear();
                
                Object Datos[]={p1.getCodArticulo() ,p1.getNomeArticulo(), p1.getCategoria(), p1.getStock(), p1.getPrecioSin(), p1.getIVA(), redondear.redondearDecimales(p1.getPrecioSin()+p1.getIVA()) };
                modelo.addRow(Datos);
                
            }
            
            if(cont==0){
                
                switch(PaListarProducto.salir){
                    
                    case 0:
                        errores.errorProductos PaAvisoProductos = new errores.errorProductos(new javax.swing.JDialog(), true);
                        PaAvisoProductos.setVisible(true);
                    break;
                    
                    case 1:
                        PaListarProducto.salir = 0;
                        dispose();
                    break;
                
                }
            
            }
            
        }
        catch(SQLException Ex){
            this.errores.setText("Lo sentimos no podemos conectar con la base de datos");
            System.err.println("Problemas al conectar con la DB");
        }
    }//GEN-LAST:event_formWindowOpened

